const apeCoinContractAddress = '0x328507DC29C95c170B56a1b3A758eB7a9E73455c'; // Add the address of your ApeCoin contract here
const provider = new ethers.providers.JsonRpcProvider('https://goerli.infura.io/v3/811a125d76e249df87d2243c26ed4ffa');
const erc20Contract = new ethers.Contract(apeCoinContractAddress, abi, provider);

function App() {
  const [isLoading, setIsLoading] = useState(false);

  const handleStake = async () => {
    try {
      setIsLoading(true);
      const approvalAmount = parseEther('1000');
      await erc20Contract.approve(stakingContractAddress, approvalAmount);
      setIsLoading(false);
      alert('Stake successful!');
    } catch (error) {
      setIsLoading(false);
      console.error(error);
      alert('Stake failed. Please check the console for error details.');
    }
  };

  const handleDeStake = async () => {
    try {
      setIsLoading(true);
      const contract = new ethers.Contract(stakingContractAddress, abi, provider);
      const amountToWithdraw = parseEther('5.0'); // 5 ApeCoin
      await contract.withdrawSelfApeCoin(amountToWithdraw);
      setIsLoading(false);
      alert('DeStake successful!');
    } catch (error) {
      setIsLoading(false);
      console.error(error);
      alert('DeStake failed. Please check the console for error details.');
    }
  };

  return (
    <div className="flex flex-col items-center justify-center min-h-screen bg-gray-100">
      <Button
        onClick={handleStake}
        isLoading={isLoading}
        loadingText="Staking..."
        colorScheme="teal"
        size="lg"
        m={4}
      >
        Stake
      </Button>
      <Button
        onClick={handleDeStake}
        isLoading={isLoading}
        loadingText="DeStaking..."
        colorScheme="red"
        size="lg"
        m={4}
      >
        DeStake
      </Button>
    </div>
  );
}

export default App;